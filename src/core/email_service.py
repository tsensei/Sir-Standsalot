import resend
from datetime import datetime
from config import FROM_EMAIL, TO_EMAILS_LIST, RESEND_API_KEY, TIMEZONE


class EmailService:
    def __init__(self):
        """Initialize the email service with Resend API key"""
        if RESEND_API_KEY:
            resend.api_key = RESEND_API_KEY
            self.enabled = True
        else:
            self.enabled = False
            print("‚ö†Ô∏è Email service disabled - RESEND_API_KEY not configured")

    def is_configured(self):
        """Check if email service is properly configured"""
        return (self.enabled and 
                FROM_EMAIL and 
                TO_EMAILS_LIST and 
                RESEND_API_KEY)

    async def send_async_updates_email(self, bot):
        """Send email with async updates only"""
        if not self.is_configured():
            print("‚ö†Ô∏è Email service not configured - skipping async updates email")
            return False

        try:
            # Generate email content
            subject, html_content, text_content = self._generate_email_content(bot)
            
            # Send email
            params = {
                "from": FROM_EMAIL,
                "to": TO_EMAILS_LIST,
                "subject": subject,
                "html": html_content,
                "text": text_content,
            }
            
            email = resend.Emails.send(params)
            print(f"‚úÖ Async updates email sent successfully! ID: {email['id']}")
            return True
            
        except Exception as e:
            print(f"‚ùå Failed to send async updates email: {e}")
            return False

